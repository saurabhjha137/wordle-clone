export const VALID_GUESSES_BY_LENGTH = Object.fromEntries(
  Object.entries(WORD_POOLS).map(([len, words]) => [len, new Set(words)])
);

export function getWordByIndex(idx, length) {
  const pool = WORD_POOLS[length];
  // Integer hash — shuffles the order so it's not sequential
  let h = idx ^ (idx >>> 16);
  h = Math.imul(h, 0x45d9f3b);
  h = h ^ (h >>> 16);
  h = Math.imul(h, 0x45d9f3b);
  h = h ^ (h >>> 16);
  return pool[Math.abs(h) % pool.length].toUpperCase();
}
